// Image Upload Preview
function previewPhoto(event) {
    const file = event.target.files[0];
    const preview = document.getElementById('upload-preview');
    const diagnosisResult = document.getElementById('diagnosis-result');
    preview.innerHTML = ''; // Clear previous preview
    diagnosisResult.textContent = ''; // Clear previous diagnosis

    if (!file) {
        preview.textContent = 'No image selected';
        return;
    }
    const previewImage = document.createElement('img');
    const fileReader = new FileReader();
    fileReader.onload = async function () {
        previewImage.src = fileReader.result;
        preview.appendChild(previewImage);

        // Collect user info
        const age = document.getElementById('age').value;
        const gender = document.getElementById('gender').value;
        const skinType = document.getElementById('skinType').value;

        // Prepare FormData for sending the file and user info to the backend
        const formData = new FormData();
        formData.append('file', file);
        formData.append('age', age);
        formData.append('gender', gender);
        formData.append('skinType', skinType);

        try {
            const response = await fetch('http://127.0.0.1:5000/predict', {
                method: 'POST',
                body: formData,
            });

            if (!response.ok) {
                throw new Error(`HTTP error! status: ${response.status}`);
            }

            const result = await response.json();

            if (result.prediction) {
                diagnosisResult.textContent = `Diagnosis: ${result.prediction} (${result.confidence})`;
            } else if (result.error) {
                diagnosisResult.textContent = `Error: ${result.error}`;
            } else {
                diagnosisResult.textContent = "Could not get a diagnosis";
            }


        } catch (error) {
            console.error("Error during prediction:", error);
            diagnosisResult.textContent = "Failed to get diagnosis.";
        }
    };
    fileReader.readAsDataURL(file);

}

