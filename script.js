function updateTextContent(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }

    });
    // Update placeholder text in the search bar
    const searchBar = document.getElementById('search-bar');
    if (searchBar && translations[lang] && translations[lang]['Search for a disease...']) {
        searchBar.setAttribute('placeholder', translations[lang]['Search for a disease...']);
    }

    // Update diagnosis text
    const diagnosisText = document.getElementById('diagnosis-result');
    if (diagnosisText && translations[lang] && translations[lang]['Diagnosis: ']) {
        let diagnosisTextString = diagnosisText.textContent
        if (diagnosisTextString.startsWith("Diagnosis:")) {
            const diagnosisValue = diagnosisTextString.substring(diagnosisTextString.indexOf(":") + 2).trim();
            diagnosisText.textContent = translations[lang]['Diagnosis: '] + diagnosisValue
        }

    }


    // Update the language button

}
